package com.dhee.entity;

public class XssEntity {
	private String id;
	private String xss;
	/**
	 * @return the id
	 */
	public String getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(String id) {
		this.id = id;
	}
	/**
	 * @return the xss
	 */
	public String getXss() {
		return xss;
	}
	/**
	 * @param xss the xss to set
	 */
	public void setXss(String xss) {
		this.xss = xss;
	}
	
}
